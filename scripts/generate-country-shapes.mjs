// Dev-only generator. Reads Natural Earth country geometry (via world-atlas),
// fits each of our 10 sourcing countries independently into a shared 100×100
// box, and emits src/data/countryShapes.ts (committed; NOT run at build time).
//
//   node scripts/generate-country-shapes.mjs
//
// Deps are devDependencies: d3-geo, topojson-client, world-atlas.
import { readFileSync, writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { geoMercator, geoPath, geoArea, geoCentroid } from "d3-geo";
import { feature } from "topojson-client";

// Natural Earth groups overseas territories under the sovereign state (e.g.
// the Dutch Caribbean under "Netherlands"), which wrecks a fitExtent. Keep only
// polygons whose centroid sits within ~30° of the main (largest) landmass —
// this preserves Sicily/Sardinia/Tasmania but drops distant island territories.
function mainland(f) {
  const polys =
    f.geometry.type === "Polygon" ? [f.geometry.coordinates] : f.geometry.coordinates;
  if (polys.length === 1) return f;
  const parts = polys.map((coordinates) => {
    const pf = { type: "Feature", geometry: { type: "Polygon", coordinates } };
    return { coordinates, area: geoArea(pf), c: geoCentroid(pf) };
  });
  const main = parts.reduce((a, b) => (b.area > a.area ? b : a));
  const kept = parts
    .filter((p) => Math.hypot(p.c[0] - main.c[0], p.c[1] - main.c[1]) < 30)
    .map((p) => p.coordinates);
  return { type: "Feature", geometry: { type: "MultiPolygon", coordinates: kept } };
}

const require = createRequire(import.meta.url);
// Prefer the coarse 110m geometry (small data, plenty of detail at ~180px);
// fall back to 50m for small countries 110m omits (e.g. Singapore).
const load = (file) => {
  const t = require(`world-atlas/${file}`);
  return feature(t, t.objects.countries).features;
};
const c110 = load("countries-110m.json");
const c50 = load("countries-50m.json");

// key → { iso (numeric ISO 3166-1), marker city [lon, lat] }
const TARGETS = {
  netherlands: { iso: "528", city: [4.9, 52.37] },   // Amsterdam
  belgium:     { iso: "56",  city: [4.35, 50.85] },  // Brussels
  italy:       { iso: "380", city: [12.5, 41.9] },   // Rome
  uae:         { iso: "784", city: [55.27, 25.2] },  // Dubai
  india:       { iso: "356", city: [77.21, 28.61] }, // New Delhi
  china:       { iso: "156", city: [116.4, 39.9] },  // Beijing
  thailand:    { iso: "764", city: [100.5, 13.75] }, // Bangkok
  singapore:   { iso: "702", city: [103.82, 1.35] }, // Singapore
  australia:   { iso: "36",  city: [151.21, -33.87] }, // Sydney
  brazil:      { iso: "76",  city: [-46.63, -23.55] }, // São Paulo
};

const BOX = [[6, 6], [94, 94]]; // padding inside the 100×100 viewBox
// 0–100 viewBox renders at ~1.8px/unit on-card, so 1 decimal (~0.18px) is
// sub-pixel — drop precision to keep the shipped data small.
const round = (s) => s.replace(/-?\d+\.?\d*/g, (n) => String(+(+n).toFixed(1)));

const out = {};
for (const [key, { iso, city }] of Object.entries(TARGETS)) {
  const id3 = iso.padStart(3, "0");
  const raw = c110.find((c) => c.id === id3) || c50.find((c) => c.id === id3);
  if (!raw) {
    console.warn(`! ${key} (iso ${id3}) not found — skipping`);
    continue;
  }
  const f = mainland(raw);
  const projection = geoMercator().fitExtent(BOX, f);
  const path = geoPath(projection);
  const d = round(path(f));
  const [mx, my] = projection(city).map((n) => +n.toFixed(2));
  out[key] = { d, marker: [mx, my] };
  console.log(`✓ ${key.padEnd(12)} ${d.length} chars  marker [${mx}, ${my}]`);
}

const body = `// AUTO-GENERATED by scripts/generate-country-shapes.mjs — do not edit by hand.
// Per-country silhouettes fit into a shared viewBox="0 0 100 100"; \`marker\` is
// the projected sourcing city in the same 0–100 space.

export type CountryShape = { d: string; marker: [number, number] };

export const COUNTRY_SHAPES: Record<string, CountryShape> = {
${Object.entries(out)
  .map(([k, v]) => `  ${k}: { d: "${v.d}", marker: [${v.marker[0]}, ${v.marker[1]}] },`)
  .join("\n")}
};
`;

writeFileSync("src/data/countryShapes.ts", body);
console.log(`\nWrote src/data/countryShapes.ts (${Object.keys(out).length} countries)`);

// ── Detailed Sri Lanka distribution map (WhyChooseUs panel) ──────────────────
// Uses the 50m coastline for detail, fit to the panel's 200×240 viewBox, with
// the seven delivery cities projected to their real geographic positions.
{
  const SL_BOX = [[55, 12], [145, 212]]; // within viewBox="0 0 200 240"
  const SL_CITIES = [
    { name: "Colombo",    lonlat: [79.86, 6.93], delay: 0 },
    { name: "Kandy",      lonlat: [80.64, 7.29], delay: 0.25 },
    { name: "Galle",      lonlat: [80.22, 6.05], delay: 0.4 },
    { name: "Jaffna",     lonlat: [80.01, 9.66], delay: 0.55 },
    { name: "Batticaloa", lonlat: [81.70, 7.72], delay: 0.7 },
    { name: "Kalutara",   lonlat: [79.96, 6.58], delay: 0.15 },
    { name: "Matara",     lonlat: [80.54, 5.95], delay: 0.5 },
  ];
  const raw = c50.find((c) => c.id === "144");
  if (!raw) {
    console.warn("! Sri Lanka (144) not found in 50m — skipping detailed map");
  } else {
    const f = mainland(raw);
    const proj = geoMercator().fitExtent(SL_BOX, f);
    const d = round(geoPath(proj)(f));
    const cities = SL_CITIES.map((c) => {
      const [cx, cy] = proj(c.lonlat).map((n) => +n.toFixed(1));
      return { name: c.name, cx, cy, delay: c.delay };
    });
    const slBody = `// AUTO-GENERATED by scripts/generate-country-shapes.mjs — do not edit by hand.
// Detailed Sri Lanka coastline + delivery cities, projected into the
// WhyChooseUs map panel's viewBox="0 0 200 240".

export type SriLankaCity = { name: string; cx: number; cy: number; delay: number };

export const SRI_LANKA_PATH = "${d}";

export const SRI_LANKA_CITIES: SriLankaCity[] = [
${cities.map((c) => `  { name: "${c.name}", cx: ${c.cx}, cy: ${c.cy}, delay: ${c.delay} },`).join("\n")}
];
`;
    writeFileSync("src/data/sriLankaMap.ts", slBody);
    console.log(`Wrote src/data/sriLankaMap.ts (path ${d.length} chars, ${cities.length} cities)`);
  }
}
